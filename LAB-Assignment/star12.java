class star12
{

	public static void main(String args[])
	{
		int i,j;
		System.out.println();
		for(i=5;i>=1;i--)//rows
		{
			for(j=1;j<=i;j++)//columns
			{
				System.out.print("*");
			}
			System.out.println();
		
			
		}
		
		for(i=2;i<=5;i++)//rows
		{
			for(j=1;j<=i;j++)//columns
			{
				System.out.print("*");
			}
			System.out.println();
			
		}
		
	}

}
