class pattern1
{

	public static void main(String args[])
	{
		int i,j;
		System.out.println();
		for(i=1;i<=5;i++)//rows
		{
			for(j=1;j<=i;j++)//columns
			{
				System.out.print(j+" ");
			}
			System.out.println();
		
			
		}		
	}

}
